import { Button } from "@theliaison/ui/button";
import {
	DropdownMenu,
	DropdownMenuCheckboxItem,
	DropdownMenuContent,
	DropdownMenuLabel,
	DropdownMenuSeparator,
	DropdownMenuTrigger,
} from "@theliaison/ui/dropdown-menu";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@theliaison/ui/tabs";
import { File, ListFilter, PlusCircle } from "lucide-react";
import Link from "next/link";
import { getProducts } from "./actions";
import { ProductsTable } from "./products-table";

export default async function ProductsPage({
	searchParams,
}: {
	searchParams: { q: string; offset: string };
}) {
	const search = searchParams.q ?? "";
	const offset = searchParams.offset ?? 0;
	const { products, newOffset, totalProducts } = await getProducts(
		search,
		Number(offset),
	);

	return (
		<Tabs defaultValue="all">
			<div className="flex items-center">
				<TabsList className="bg-background">
					<TabsTrigger value="all">All</TabsTrigger>
					<TabsTrigger value="active">Active</TabsTrigger>
					<TabsTrigger value="draft">Draft</TabsTrigger>
					<TabsTrigger value="archived" className="hidden sm:flex">
						Archived
					</TabsTrigger>
				</TabsList>
				<div className="ml-auto flex items-center gap-2">
					<DropdownMenu>
						<DropdownMenuTrigger asChild>
							<Button variant="outline" size="sm" className="h-8 gap-1 bg-foreground text-background border-none">
								<ListFilter className="h-3.5 w-3.5" />
								<span className="sr-only sm:not-sr-only sm:whitespace-nowrap">
									Filter
								</span>
							</Button>
						</DropdownMenuTrigger>
						<DropdownMenuContent align="end">
							<DropdownMenuLabel>Filter by</DropdownMenuLabel>
							<DropdownMenuSeparator />
							<DropdownMenuCheckboxItem checked>
								Active
							</DropdownMenuCheckboxItem>
							<DropdownMenuCheckboxItem>Draft</DropdownMenuCheckboxItem>
							<DropdownMenuCheckboxItem>Archived</DropdownMenuCheckboxItem>
						</DropdownMenuContent>
					</DropdownMenu>
					<Button size="sm" variant="outline" className="h-8 gap-1 bg-foreground text-background border-none">
						<File className="h-3.5 w-3.5" />
						<span className="sr-only sm:not-sr-only sm:whitespace-nowrap">
							Export
						</span>
					</Button>
					<Button size="sm" className="h-8 gap-1" asChild>
						<Link href="/dashboard/products/create">
							<PlusCircle className="h-3.5 w-3.5" />
							<span className="sr-only sm:not-sr-only sm:whitespace-nowrap">
								Add Product
							</span>
						</Link>
					</Button>
				</div>
			</div>
			<TabsContent value="all">
				<ProductsTable
					products={products}
					offset={newOffset ?? 0}
					totalProducts={totalProducts}
				/>
			</TabsContent>
		</Tabs>
	);
}
